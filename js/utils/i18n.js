class I18n {
    constructor() {
        this.lang = localStorage.getItem('gipfel-lang') || 'de';
        this.init();
    }

    init() {
        document.addEventListener('DOMContentLoaded', () => {
            this.setLanguage(this.lang);
            this.setupListeners();
        });
    }

    setupListeners() {
        const toggle = document.querySelector('.lang-toggle');
        const dropdown = document.querySelector('.lang-dropdown');

        if (toggle && dropdown) {
            toggle.addEventListener('click', (e) => {
                e.stopPropagation();
                dropdown.classList.toggle('is-open');
                const isOpen = dropdown.classList.contains('is-open');
                toggle.setAttribute('aria-expanded', isOpen);
            });

            document.addEventListener('click', (e) => {
                if (!dropdown.contains(e.target)) {
                    dropdown.classList.remove('is-open');
                    toggle.setAttribute('aria-expanded', 'false');
                }
            });
        }

        document.querySelectorAll('[data-lang-switch]').forEach(btn => {
            btn.addEventListener('click', (e) => {
                e.preventDefault();
                const lang = btn.getAttribute('data-lang-switch');
                this.setLanguage(lang);
                if (dropdown) dropdown.classList.remove('is-open');
            });
        });
    }

    setLanguage(lang) {
        this.lang = lang;
        localStorage.setItem('gipfel-lang', lang);
        
        // Update document lang attribute
        document.documentElement.lang = lang;

        // Update active label in toggle if it exists
        const activeLabel = document.querySelector('.active-lang');
        if (activeLabel) {
            activeLabel.innerText = lang.toUpperCase();
        }

        // Update all translatable elements
        document.querySelectorAll('[data-i18n]').forEach(el => {
            const key = el.getAttribute('data-i18n');
            if (translations[lang] && translations[lang][key]) {
                el.innerText = translations[lang][key];
            }
        });

        // Update active class on language switchers
        document.querySelectorAll('[data-lang-switch]').forEach(btn => {
            if (btn.getAttribute('data-lang-switch') === lang) {
                btn.classList.add('is-active');
            } else {
                btn.classList.remove('is-active');
            }
        });

        // Broadcast event for other scripts
        document.dispatchEvent(new CustomEvent('languageChanged', { detail: lang }));
    }

    t(key) {
        return (translations[this.lang] && translations[this.lang][key]) || key;
    }
}

// Initialize I18n
const i18n = new I18n();
window.i18n = i18n; // Global access if needed
